const addNumberBtn = document.getElementById('addNumberBtn');
const warningMsg = document.getElementById('warningMsg');
const calledNumbersContainer = document.getElementById('calledNumbersContainer');
const resultsContainer = document.getElementById('resultsContainer');
const tablesContainer = document.getElementById('tablesContainer');
const clearNumbersBtn = document.getElementById('clearNumbersBtn');

/*
  We store called numbers as strings in drawnNumbers.
*/
let drawnNumbers = [];

/*
  We store references to all <td> cells in this map:
  Key = tableName + '_' + number
  Value = array of <td> elements that contain that number (for that table).
*/
const domCellMap = {};

/********************************************
 * 4) HELPER: "CLOSEST TO WIN" SCORE
 ********************************************/
/*
  For each tableData, we compute how many 
  numbers are NOT called in each subsection 
  and take the MIN of those counts.

  The lower the min -> the closer the table 
  is to having at least one winning subsection.
*/
function getClosestWinScore(tableData, drawnSet) {
  let minUncalled = Infinity;
  for (let sNum = 1; sNum <= 6; sNum++) {
    const numsInSec = getSectionNumbers(tableData, sNum);
    const uncalledCount = numsInSec.filter(n => !drawnSet.has(String(n))).length;
    if (uncalledCount < minUncalled) {
      minUncalled = uncalledCount;
    }
  }
  return minUncalled;
}

/********************************************
 * 5) GET SORTED TABLE LIST
 ********************************************/
/*
  Instead of mutating the original tableObjects array,
  we generate a *new array* that is sorted by closeness.
  
  This preserves each table's original name & color,
  but reorders them *in the DOM* only.
*/
function getSortedTableList() {
  const drawnSet = new Set(drawnNumbers);

  // Attach a temporary "score" property for sorting
  const withScores = tableObjects.map(obj => {
    return {
      ...obj,
      score: getClosestWinScore(obj.data, drawnSet)
    };
  });

  // Sort ascending by score
  withScores.sort((a, b) => a.score - b.score);

  return withScores;
}

/********************************************
 * 6) RENDER TABLES (in 3-column layout)
 ********************************************/
function renderTables() {
  tablesContainer.innerHTML = '';

  // We want to reorder the *DOM* but keep original table properties
  const sortedList = getSortedTableList();

  // Clear the domCellMap every time we do a fresh render
  Object.keys(domCellMap).forEach(key => { delete domCellMap[key]; });

  // Now render each table in sorted order
  sortedList.forEach(tableObj => {
    const { data, name, colorClass } = tableObj;

    // We'll wrap each "table" in a .table-wrapper div,
    // which is placed into the #tablesContainer (which is a 3-col grid).
    const wrapperDiv = document.createElement('div');
    wrapperDiv.classList.add('table-wrapper');

    // Create the actual <table> in the DOM
    const tableEl = document.createElement('table');
    tableEl.classList.add('bingo-table', colorClass);

    // Table caption with the original name
    const caption = document.createElement('caption');
    caption.textContent = name;
    tableEl.appendChild(caption);

    // Create rows/cells
    for (let row = 0; row < data.length; row++) {
      const rowEl = document.createElement('tr');
      for (let col = 0; col < data[row].length; col++) {
        const cellValue = data[row][col];
        const td = document.createElement('td');
        td.textContent = cellValue;

        // Determine which subsection for coloring
        const sIndex = sectionsDefinition.findIndex(s => 
          row >= s.rowStart && row <= s.rowEnd &&
          col >= s.colStart && col <= s.colEnd
        );
        const sNum = sIndex + 1;
        if (sNum === 1 || sNum === 4 || sNum === 5) {
          td.classList.add('sub-lighter');
        } else {
          td.classList.add('sub-normal');
        }

        // Register this <td> in our domCellMap
        const key = name + '_' + String(cellValue);
        if (!domCellMap[key]) {
          domCellMap[key] = [];
        }
        domCellMap[key].push(td);

        rowEl.appendChild(td);
      }
      tableEl.appendChild(rowEl);
    }

    wrapperDiv.appendChild(tableEl);
    tablesContainer.appendChild(wrapperDiv);
  });
}

/********************************************
 * 7) ADD A CALLED NUMBER
 ********************************************/
function addCalledNumber() {
  warningMsg.textContent = '';

  const userInput = singleNumberInput.value.trim();
  if (!userInput) return;

  const number = parseInt(userInput, 10);
  if (isNaN(number) || number < 1 || number > 90) {
    warningMsg.textContent = 'Number must be between 1 and 90!';
    return;
  }

  if (drawnNumbers.includes(userInput)) {
    warningMsg.textContent = `Number "${userInput}" is already called!`;
    return;
  }

  // 1) Add the new number to the drawnNumbers
  drawnNumbers.push(userInput);

  // 2) Re-render tables in the new sorted order
  renderTables();

  // 3) Highlight *all* called numbers (since we recreated the DOM)
  drawnNumbers.forEach(nStr => highlightNumber(nStr));

  // 4) Render the tags + check winners
  renderCalledNumberTags();
  updateWinners();

  singleNumberInput.value = '';
}

/********************************************
 * 8) REMOVE A CALLED NUMBER
 ********************************************/
function removeCalledNumber(numStr) {
  // 1) Remove from drawnNumbers
  drawnNumbers = drawnNumbers.filter(n => n !== numStr);

  // 2) Re-render tables (sorted)
  renderTables();

  // 3) Re-highlight all existing called numbers
  drawnNumbers.forEach(nStr => highlightNumber(nStr));

  // 4) Update called tags + winners
  renderCalledNumberTags();
  updateWinners();
}

/********************************************
 * 9) HIGHLIGHT A CALLED NUMBER
 ********************************************/
function highlightNumber(numStr) {
  // We highlight *all* cells that match 
  // any table's name + '_' + numStr
  Object.keys(domCellMap).forEach(key => {
    if (key.endsWith('_' + numStr)) {
      domCellMap[key].forEach(td => td.classList.add('highlight'));
    }
  });
}

/********************************************
 * 10) CALLED NUMBERS TAGS
 ********************************************/
function renderCalledNumberTags() {
  calledNumbersContainer.innerHTML = '';

  if (drawnNumbers.length === 0) {
    calledNumbersContainer.innerHTML = '<p>No numbers called yet.</p>';
    return;
  }

  drawnNumbers.forEach(num => {
    const tagEl = createCalledNumberTag(num);
    calledNumbersContainer.appendChild(tagEl);
  });
}

function createCalledNumberTag(numStr) {
  const span = document.createElement('span');
  span.classList.add('called-number-tag');
  span.textContent = numStr;

  const removeIcon = document.createElement('span');
  removeIcon.classList.add('remove-icon');
  removeIcon.textContent = '';

  removeIcon.addEventListener('click', e => {
    e.stopPropagation();
    removeCalledNumber(numStr);
  });

  span.appendChild(removeIcon);
  return span;
}

/********************************************
 * 11) CHECK WINNERS
 ********************************************/
function updateWinners() {
  const drawnSet = new Set(drawnNumbers);
  let winnerCount = 0;
  const messages = [];

  // Let's use the same sorted order that we rendered in
  const sortedList = getSortedTableList();

  sortedList.forEach(tableObj => {
    const { data, name } = tableObj;

    // Check each of the 6 subsections
    for (let sNum = 1; sNum <= 6; sNum++) {
      const numsInSection = getSectionNumbers(data, sNum);
      const isWinning = numsInSection.every(n => drawnSet.has(String(n)));
      if (isWinning) {
        winnerCount++;
        messages.push(`${name} - Section ${sNum} is WINNING!`);
      }
    }
  });

  if (winnerCount === 0) {
    resultsContainer.innerHTML = '<p>No winners yet.</p>';
  } else {
    resultsContainer.innerHTML = '';
    messages.forEach(msg => {
      const p = document.createElement('p');
      p.classList.add('winner');
      p.textContent = msg;
      resultsContainer.appendChild(p);
    });
  }
}

/********************************************
 * 12) CLEAR ALL NUMBERS
 ********************************************/
clearNumbersBtn.addEventListener('click', () => {
  const confirmed = window.confirm('Are you sure you want to clear all called numbers?');
  if (confirmed) {
    // 1) Empty drawnNumbers
    drawnNumbers = [];

    // 2) Re-render tables (theyll still sort, 
    //    but effectively all have the same score now)
    renderTables();

    // 3) Clear the called number tags
    renderCalledNumberTags();

    // 4) No highlights
    Object.values(domCellMap).forEach(tdArray => {
      tdArray.forEach(td => td.classList.remove('highlight'));
    });

    // 5) Update winners + clear warnings
    updateWinners();
    warningMsg.textContent = '';
  }
});

/********************************************
 * 13) EVENT LISTENERS
 ********************************************/
addNumberBtn.addEventListener('click', addCalledNumber);
singleNumberInput.addEventListener('keypress', e => {
  if (e.key === 'Enter') {
    addCalledNumber();
  }
});

/********************************************
 * 14) ON PAGE LOAD
 ********************************************/
window.addEventListener('DOMContentLoaded', () => {
  renderTables(); // initial render
});

